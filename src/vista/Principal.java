    private void cargarCategorias() {
        ListaDoblementeEnlazadaGenerica listaCategorias = controlador.obtenerCategorias();

        ObjetoGenerico og = listaCategorias.getCabecera();
        
        while (og != null) {
            DTOCategoria categoria = (DTOCategoria) og.getT();
           
            cbxCategorias.addItem(String.format("%d %s", categoria.getId(), categoria.getNombre()));

            og = og.getSiguiente();
        }
    }

