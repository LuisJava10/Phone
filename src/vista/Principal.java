    private void btnProductoAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProductoAgregarActionPerformed
        String nombre = txtProductoNombre.getText().trim();
        String precio = txtProductoPrecio.getText().trim();
        String stock = txtProductoStock.getText().trim();
        
        if (nombre.isEmpty() || precio.isEmpty() || stock.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos los campos para crear producto son obligatorios.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            Double.parseDouble(precio);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El precio del producto debe ser un número.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            Integer.parseInt(stock);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El stock del producto debe ser un número.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        DTOArticulo articulo = new DTOArticulo();
        articulo.setNombre(nombre);
        articulo.setPrecio(Double.parseDouble(precio));
        articulo.setStock(Integer.parseInt(stock));
        
        String categoriaId = cbxCategorias.getSelectedItem().toString().split(" ")[0];
        
        articulo.setCategoriaId(Integer.parseInt(categoriaId));
        articulo.setComando(ComandosSql.ARTICULO_CREAR);
        
        controlador.crearArticulo(articulo);
        
        cargarArticulos();
    }//GEN-LAST:event_btnProductoAgregarActionPerformed

